    private void BtnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCreateActionPerformed
        // TODO add your handling code here:
        String Username = txtUsername.getText();
        String Password= txtPassword.getText();
        String Name = txtName.getText();
        String Phone = txtPhone.getText();
        String Email = txtEmail.getText();
        CreateUserAccountController CUA = new CreateUserAccountController();
        CUA.Username=Username;
        CUA.Name=Name;
        CUA.Password=Password;
        CUA.Email=Email;
        CUA.Phone=Phone;
        CUA.Roles=(String)txtRole.getSelectedItem();
        if (Sta == 1) {
            CUA.Status="Open";
        } else {
            CUA.Status="Close";
        }
        CUA.createAccountController();
        setVisible(false);
        new UserAdminManageUserAccountPage().setVisible(true);
    }//GEN-LAST:event_BtnCreateActionPerformed

