    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
          DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
          Iterator<Account> allacc=ViewUserAccountController.getAlldata();
          Iterator<Role> allacc2=ViewUserAccountController.getAllRoles();
          while(allacc2.hasNext()){
              Role roles = allacc2.next();
              txtRole.addItem(roles.getRoleName());
          }
          while (allacc.hasNext()) {
              
                    Account accountObj = allacc.next();
                    dtm.addRow(new Object[]{accountObj.getAccId(), accountObj.getAccUsername(), accountObj.getAccPassword(), accountObj.getAccName(), accountObj.getAccPhone(), accountObj.getAccEmail(), accountObj.getAccRole(), accountObj.getStatus()});
                }
    }//GEN-LAST:event_formComponentShown

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String AccId = model.getValueAt(index, 0).toString();
        lblAccId.setText(AccId);
        String AccUsername = model.getValueAt(index, 1).toString();
        txtUsername.setText(AccUsername);
        txtUsername.setEditable(false);
        String AccPassword = model.getValueAt(index, 2).toString();
        txtPassword.setText(AccPassword);
        String AccName = model.getValueAt(index, 3).toString();
        txtName.setText(AccName);
        String AccPhone = model.getValueAt(index, 4).toString();
        txtPhone.setText(AccPhone);
        String AccEmail = model.getValueAt(index, 5).toString();
        txtEmail.setText(AccEmail);
        String AccRole = model.getValueAt(index, 6).toString();
        txtRole.setSelectedItem(AccRole);
        String status = model.getValueAt(index, 7).toString();

        if (status.equals("Open")) {
            Open.setSelected(true);
        } else {
            Close.setSelected(true);
        }
        BtnModify.setEnabled(true);
        BtnDelete.setEnabled(true);
        BtnClear.setEnabled(true);
    }//GEN-LAST:event_jTable1MouseClicked

